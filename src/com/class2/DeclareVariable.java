package com.class2;

public class DeclareVariable {

	public static void main(String[] args) {
		// different ways to create/declare variable;
		// 1. create variable and assign value
		int num = 28;

		// 2 ways: using 2 steps
		int y; // 1. declaring variable
		y = 12; // 2. assign value

		// declare variable 1 by 1
		int a;
		int b;
		int c;
		// or all together
		int x, n, m, f;

		a = 10;
		b = 20;
		c = 30;

	}

}
